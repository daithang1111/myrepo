PRAGMA encoding = "UTF-8";
DROP TABLE IF EXISTS DOCUMENT;
CREATE TABLE DOCUMENT (
    rowID       INTEGER PRIMARY KEY AUTOINCREMENT,
    GROUPID	varchar(100),
    ACTORID		varchar(100),
    DOCID      varchar(1000),
    DOCTITLE	varchar(1000),
    DOCTIME		varchar(100),
    DOCCONTENT      text

);
CREATE INDEX X_DOC_ID ON DOCUMENT (DOCID);